    track_width = params['track_width']
    distance_from_center = params['distance_from_center']
    heading = params['heading']
    speed = params['speed']

    # Calculate the reward
    reward = 1.0

    # Penalize if the car is off the track
    if not all_wheels_on_track:
        reward = 1e-3
